# Variables Python
'''
Las variables son espacios de memoria que almacenan valores
o datos de distintos tipos, y (como su nombre indica) pueden
variar. Se crean en el momento que se les asigna un valor, por
lo cual en Python no requerimos declararlas previamente.
'''

# strings
name = 'mateo'
print(name)

# strings
name = 'mateo david'
print(name)

# numbers
age = 32
print(age)

# Add values
sumNum = 23 + 33
print(sumNum)

# Use input
braking_bad = input('Say my name: ')
print(braking_bad)

# Concatenate values
name_son = 'mateo'
lastname_son = 'cervellon'
concat_name = name_son + ' ' + lastname_son
print(concat_name)
