# implicit conversions

num1 = 20
num2 = 30.5
num1 = num1 + num2
print(type(num1))
print(type(num2))

# explicit conversions
numer01 = 5.8
print(numer01)
numero2 = int(numer01)
print(numero2)
print(type(numero2))

# Input
age = int(input('Cual es tu edad: '))
print(age)
print(type(age))
print(f'Tu edad es {age + 2}')
