'''
Method Index
Utilizamos el método index( ) para explorar strings, ya que
permite hallar el índice de aparición de un caracter o cadena de
caracteres dentro de un texto dado.
'''

my_text = 'hola mateo, como estas, espero te encuentres muy pero muy bien, hasta pronto!'

print(my_text.index('m', 6, 20))
print(my_text[2:10])
